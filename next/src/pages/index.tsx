const GET_USERS = gql`
  query GetUsers {
    allUsers {
      id
      name
    }
  }
`
export async function getServerSideProps() {
  const client = getApolloClient()

  try {
    const { data } = await client.query({
      query: GET_USERS,
    })

    return {
      props: {
        initialUsers: data.allUsers,
      },
    }
  } catch (error) {
    console.error('Error fetching users:', error)
    return {
      props: {
        initialUsers: [],
      },
    }
  }
}

